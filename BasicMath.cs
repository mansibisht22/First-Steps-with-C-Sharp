using System;
class BasicMath
{
    static void Main()
    {
        int sum = 10+10;
        int diff = 100-20;
        int prod = 20*4;
        int quot = 400/8;
        int rem = 20%3;

        Console.WriteLine("Sum: " + sum);
        Console.WriteLine("Difference: " + diff);
        Console.WriteLine("Product: " + prod);
        Console.WriteLine("Quotient: " + quot);
        Console.WriteLine("Remainder: " + rem);


    }
}